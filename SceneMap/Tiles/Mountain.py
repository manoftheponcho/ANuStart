import pyglet


class Mountain:
    image_string = ''.join(['{3}{3}{2}{0}{3}{3}{3}{3}{0}{3}{0}{0}{0}{3}{0}{3}',
                            '{2}{3}{2}{3}{3}{3}{3}{3}{3}{3}{0}{2}{0}{3}{2}{3}',
                            '{0}{2}{2}{2}{2}{3}{3}{0}{3}{0}{2}{2}{3}{3}{2}{2}',
                            '{0}{3}{2}{2}{3}{3}{0}{0}{3}{3}{2}{3}{3}{0}{3}{2}',
                            '{3}{3}{3}{3}{3}{2}{0}{2}{3}{2}{3}{3}{0}{3}{0}{0}',
                            '{3}{3}{0}{3}{2}{2}{3}{3}{2}{3}{3}{0}{0}{3}{3}{3}',
                            '{2}{3}{3}{0}{0}{3}{2}{3}{3}{3}{0}{2}{2}{3}{0}{3}',
                            '{0}{3}{3}{0}{3}{3}{2}{0}{3}{0}{2}{2}{3}{3}{3}{0}',
                            '{3}{3}{2}{0}{3}{3}{3}{3}{3}{3}{3}{3}{3}{3}{3}{0}',
                            '{2}{3}{2}{3}{3}{3}{3}{3}{3}{3}{0}{0}{3}{3}{3}{3}',
                            '{0}{2}{2}{2}{2}{3}{3}{0}{3}{0}{3}{3}{3}{3}{3}{3}',
                            '{0}{3}{2}{2}{3}{3}{0}{0}{3}{3}{3}{0}{3}{3}{0}{3}',
                            '{3}{3}{3}{3}{3}{2}{0}{2}{3}{3}{0}{0}{3}{3}{0}{0}',
                            '{3}{3}{0}{3}{2}{2}{3}{3}{0}{3}{2}{2}{3}{0}{3}{0}',
                            '{2}{3}{3}{0}{0}{3}{2}{3}{3}{3}{2}{3}{0}{0}{3}{3}',
                            '{0}{3}{3}{0}{3}{3}{2}{0}{3}{3}{3}{3}{0}{3}{3}{3}'][::-1])
    image_bytes = image_string.format('\x00\x00\x00\xff', '\x0d\x93\x00\xff', '\xad\xad\xad\xff', '\xff\xff\xff\xff')
    image = pyglet.image.create(16, 16)
    image.set_data('RGBA', 64, image_bytes)

if __name__ == "__main__":
    window = pyglet.window.Window(256, 240)
    mountain = Mountain()

    @window.event
    def on_draw():
        window.clear()
        pyglet.gl.gl.glScalef(12, 12, 12)
        mountain.image.blit(0, 0, 0)

    pyglet.app.run()